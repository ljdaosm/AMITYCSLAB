//c prog to finf if number is divisible by 5 and 11
#include<stdio.h>
int main()
{
    int x;
    printf("Enter the value of X: ");
    scanf("%d",&x);
    
    if (x%5==0 && x%11==0)
    {
        printf("The number is divisible by 5 and 11");
    }
    else 
    {
        printf("Not divisible by 5 and/or 11");
    }
    return 0;
}
